export type TranslationVariables = Record<string, string | number>;

export function translate(language: AppLanguage, source: string, variables?: TranslationVariables) {
  const template = language === "en-US" ? english[source] ?? source : source;
  if (!variables) return template;
  return template.replace(/\{(\w+)\}/g, (match, key: string) => String(variables[key] ?? match));
}
